  # 결재 액션별 아이콘을 렌더링하는 헬퍼
  def approval_action_icon_svg(action, size: 'w-5 h-5', css_class: '')
    icon_name = approval_action_icon(action)
    color_class = case action
    when 'approve'
      'text-green-600'
    when 'reject'  
      'text-red-600'
    when 'view'
      'text-blue-600'
    else
      'text-gray-600'
    end
    hero_icon(icon_name, size: size, css_class: "#{color_class} #{css_class}")
  end
